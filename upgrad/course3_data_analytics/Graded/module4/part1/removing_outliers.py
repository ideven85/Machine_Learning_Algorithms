import numpy as np


def removing_outliers(arr):
    """
    Outliers formula: elements in the array which are greater than (mean-2*std) and less than (mean +2*std)
    Args:
        arr: input list

    Returns: list removing outliers

    """
    np_arr = np.array(arr)
    mean = np.mean(np_arr, axis=0)
    dev = np.std(np_arr, axis=0)

    answer = [x for x in np_arr if (x > mean - 2 * dev)]
    answer = [x for x in answer if (x < mean + 2 * dev)]
    answer = np.array(answer)
    # print(answer.shape)
    return answer


